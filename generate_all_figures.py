def savefig(fig, filename):
    fig.tight_layout()
    fig.savefig(PLOTS / filename, dpi=220, bbox_inches="tight")
    plt.close(fig)

def pchip_curve(points, n=700):
    pts = np.array(points, dtype=float)
    x, y = pts[:, 0], pts[:, 1]
    xx = np.linspace(x.min(), x.max(), n)
    yy = PchipInterpolator(x, y)(xx)
    return xx, yy

def make_figure_1():
    T = np.linspace(200, 400, 300)
    Na = Nd = 1e18

    ni_vals = ni_article(T)
    Vbi_vals = Vbi_article(Na, Nd, T)

    fig, ax = plt.subplots(figsize=(6.2, 4.5))
    ax.plot(T, Vbi_vals, linewidth=1.8)
    ax.set_xlabel("Temperature (K)")
    ax.set_ylabel(r"Built-in Potential, $V_{bi}$ (V)")
    savefig(fig, "figure_1a_built_in_potential_vs_temperature.png")

    fig, ax = plt.subplots(figsize=(6.2, 4.5))
    ax.plot(T, ni_vals, linewidth=1.8)
    ax.set_yscale("log")
    ax.set_xlabel("Temperature (K)")
    ax.set_ylabel(r"Intrinsic Carrier Concentration, $n_i$ (cm$^{-3}$)")
    ax.grid(True, which="both", alpha=0.28)
    savefig(fig, "figure_1b_intrinsic_concentration_vs_temperature.png")

def make_figure_2():
    doping = np.logspace(16, 20, 240)
    widths = np.array([W_article(N, N, Vbi_article(N, N, 300.0)) for N in doping])

    fig, ax = plt.subplots(figsize=(6.5, 4.8))
    ax.plot(doping, widths, linewidth=1.8)
    ax.set_xscale("log")
    ax.set_xlabel(r"Doping Concentration, $N_a = N_d$ (cm$^{-3}$)")
    ax.set_ylabel(r"Total Depletion Width, $W$ (m)")
    ax.grid(True, which="both", alpha=0.28)
    savefig(fig, "figure_2_depletion_width_vs_doping.png")

