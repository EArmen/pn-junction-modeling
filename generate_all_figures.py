PLOTS.mkdir(exist_ok=True)
TABLES.mkdir(exist_ok=True)

q = 1.602176634e-19
eps0 = 8.854e-12
epsr_si = 11.7
eps_si = epsr_si * eps0
kB = 1.380649e-23
h = 6.62607015e-34
m0 = 9.10938356e-31
m_eff = 1.08 * m0
CM3_TO_M3 = 1e6

def ni_article(T):
    Eg_eV = 1.12
    Eg_J = Eg_eV * q
    ni_m3 = 2.0 * ((2.0 * np.pi * m_eff * kB * T) / (h**2))**1.5 * np.exp(-Eg_J / (2.0 * kB * T))
    return ni_m3 / CM3_TO_M3

def Vbi_article(Na_cm3, Nd_cm3, T):
    ni = ni_article(T)
    return (kB * T / q) * np.log((Na_cm3 * Nd_cm3) / (ni**2))

def W_article(Na_cm3, Nd_cm3, Vbi):
    Na = Na_cm3 * CM3_TO_M3
    Nd = Nd_cm3 * CM3_TO_M3
    return np.sqrt((2.0 * eps_si * Vbi / q) * ((Na + Nd) / (Na * Nd)))

